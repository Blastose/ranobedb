export function filter(
	input: string,
	publisher?: Nullish<string>,
	label?: Nullish<string>,
): string {
	let filteredInput = input;

	for (const filler of fillerWords) {
		filteredInput = filteredInput.replaceAll(filler, '');
	}

	filteredInput = filteredInput.replace(/^小説版\s/, '');
	filteredInput = filteredInput.replaceAll(/【電子.*?】/g, '');
	filteredInput = filteredInput.replaceAll(/\s*電子DX版\s*/g, '');
	filteredInput = filteredInput.replaceAll(/\s*電子書籍特典ショートストーリー付き\s*/g, '');
	filteredInput = filteredInput.replaceAll(/【書.?下ろし・?イラスト[0-9]*.*?】/g, '');
	filteredInput = filteredInput.replaceAll(/【書.?下ろし・?本文イラスト[0-9]*.*?】/g, '');

	if (label && label !== '――') {
		filteredInput = filteredInput.replaceAll(
			new RegExp(`(（|【|\()${label}.*?(）|】|\))`, 'g'),
			'',
		);
	}
	if (publisher) {
		filteredInput = filteredInput.replaceAll(
			new RegExp(`(（|【|\()${publisher}.*?(）|】|\))`, 'g'),
			'',
		);
	}
	filteredInput = filteredInput.replace(/\(\)$/, '');
	filteredInput = filteredInput.replaceAll(/【.*?シリーズ】/g, '');
	filteredInput = filteredInput.match(/「(.*?)」シリーズ/)?.at(1) ?? filteredInput;
	return filteredInput;
}
